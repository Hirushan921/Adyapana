    public void loadSubjects() {
       try {
            ResultSet rs = MySQL.search("SELECT * FROM `subject`  ORDER BY `subject`.`subno` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable3.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("subno"));
                v.add(rs.getString("name"));
         
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    public void loadSubjects(String text) {
       try {
            ResultSet rs = MySQL.search("SELECT * FROM `subject` WHERE `name` LIKE '%"+text+"%' ORDER BY `subject`.`subno` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable3.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("subno"));
                v.add(rs.getString("name"));
         
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
