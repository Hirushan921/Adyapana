    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String subject = jTextField1.getText();
     

        if (subject.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter subject", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                ResultSet searchsub = MySQL.search("SELECT * FROM `subject` WHERE `name`='" + subject + "' ");
                if (searchsub.next()) {
                    JOptionPane.showMessageDialog(this, "Subject already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.iud("INSERT INTO `subject`(`name`) VALUES('" + subject+ "')");

                    resetFields();
                    loadSubjects();

                    JOptionPane.showMessageDialog(this, "New Subject Registered..", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

